    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
 
        int lotNumber;
        try
        {
            lotNumber = Integer.parseInt(txtLotNo.getText());
            house = new ListHouse( lotNumber, 0, 0, 0,"", "");
            if (list.isThere(house))
            {
                house = (ListHouse)list.retrieve(house);
                showHouse(house);
                lableMsg.setText("House found");
            }
            else
                lableMsg.setText("House not found");
        }
        catch (NumberFormatException badHouseData)
        {
        // Text field info incorrectly formated
        lableMsg.setText("INPUT LOT NUMBER  " + badHouseData.getMessage());
        }         // TODO add your handling code here:
    }//GEN-LAST:event_btnFindActionPerformed

