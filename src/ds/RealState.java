private static void clearHouse()
    {
        txtLotNo.setText("");
        txtFirstName.setText(""); 
        txtLastName.setText("");
        txtPrice.setText("");
        txtSqFeet.setText("");
        txtRooms.setText("");
    }
    private static void showHouse(ListHouse house)
    {
        System.out.println(""+house.getLotNumber());
        txtLotNo.setText(""+house.getLotNumber());
        txtFirstName.setText(house.getFirstName());
        txtLastName.setText(house.getLastName());
        txtPrice.setText(Integer.toString(house.getPrice()));
        txtSqFeet.setText(Integer.toString(house.getSquareFeet()));
        txtRooms.setText(Integer.toString(house.getBedRooms()));
    }
    
    private static ListHouse getHouse()
    {
        int lotNo;
        String FirstName;
        String LastName;
        int Price;
        int SqFeet;
        int rooms;
        
        lotNo = Integer.parseInt(txtLotNo.getText());
        FirstName = txtFirstName.getText();
        LastName = txtLastName.getText();
        Price = Integer.parseInt(txtPrice.getText());
        SqFeet = Integer.parseInt(txtSqFeet.getText());
        rooms = Integer.parseInt(txtRooms.getText());
        
        ListHouse house = new ListHouse(lotNo,SqFeet,rooms,Price,LastName,FirstName);
        return house;
    }

    
    
